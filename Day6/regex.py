import re

# re module бһӮбһәбһҮбһ¶бһҳбһҪбһҷ module бһҹбһҳбҹ’бһҡбһ¶бһ”бҹӢбһҖбһ¶бһҡбһҠбҹҶбһҺбһҫбһҡбһҖбһ¶бһҡ regular expressions бһҖбҹ’бһ“бһ»бһ„ Pythonбҹ”
text = "Contact me at 070-213-069"
# findall() method бһҸбҹ’бһҡбһјбһңбһ”бһ¶бһ“бһ”бҹ’бһҡбһҫбһҠбһҫбһҳбҹ’бһ”бһёбһҹбҹ’бһңбҹӮбһ„бһҡбһҖбһ“бһ·бһ„бһҷбһҖбһўбһҸбҹ’бһҗбһ”бһ‘бһҠбҹӮбһӣбһҸбҹ’бһҡбһјбһңбһӮбҹ’бһ“бһ¶бһ“бһ№бһ„ regular expression бһҠбҹӮбһӣбһ”бһ¶бһ“бһ•бҹ’бһҸбһӣбҹӢ
pattarn = re.findall(r"\d+",text)
print(pattarn)

# sub() method бһҸбҹ’бһҡбһјбһңбһ”бһ¶бһ“бһ”бҹ’бһҡбһҫбһҠбһҫбһҳбҹ’бһ”бһёбһҮбҹҶбһ“бһҪбһҹбһўбһҸбҹ’бһҗбһ”бһ‘бһҠбҹӮбһӣбһҸбҹ’бһҡбһјбһңбһӮбҹ’бһ“бһ¶бһ“бһ№бһ„ regular expression бһҮбһ¶бһҳбһҪбһҷбһўбһҸбҹ’бһҗбһ”бһ‘бһҗбҹ’бһҳбһё
updated_text = re.sub(r"\d+", "XXX-XXX-XXX", text)
print(updated_text)