#upper() method бһҸбҹ’бһҡбһјбһңбһ”бһ¶бһ“бһ”бҹ’бһҡбһҫбһҠбһҫбһҳбҹ’бһ”бһёбһ”бһҳбҹ’бһӣбҹӮбһ„бһўбһҖбҹ’бһҹбһҡбһ‘бһ¶бҹҶбһ„бһўбһҹбҹӢбһҖбҹ’бһ“бһ»бһ„бһҹбҹ’бһ‘бҹ’бһҡбһёбһ„бһ‘бҹ…бһҮбһ¶бһўбһҖбҹ’бһҹбһҡбһ’бҹҶ
